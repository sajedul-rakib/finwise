  static List<Map<String, dynamic>> getAll() => _allIcons;

  static List<Map<String, dynamic>> search(String query) {
    if (query.isEmpty) return _allIcons;

    final lowercaseQuery = query.toLowerCase();

    return _allIcons.where((icon) {
      final title = icon['title'].toString().toLowerCase();
      final tags = (icon['tags'] as List<String>);

      return title.contains(lowercaseQuery) ||
          tags.any((tag) => tag.toLowerCase().contains(lowercaseQuery));
    }).toList();
  }

  static Map<String, dynamic>? getById(String id) {
    try {
      return _allIcons.firstWhere((icon) => icon['id'] == id);
    } catch (e) {
      return null; // Return null if ID doesn't exist
    }
  }

  static List<Map<String, dynamic>> getByTag(String tag) {
    return _allIcons.where((icon) {
      final List<String> tags = icon['tags'];
      return tags.contains(tag.toLowerCase());
    }).toList();
  }

  static List<Map<String, dynamic>> getPage(int page, int pageSize) {
    int start = page * pageSize;
    int end = start + pageSize;

    if (start >= _allIcons.length) return [];
    return _allIcons.sublist(
      start,
      end > _allIcons.length ? _allIcons.length : end,
    );
  }
